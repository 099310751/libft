#include<stddef.h>

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = b;
	while (len > 0)
	{
		ptr[i] = c;
		i++;
		len--;
	}
	return (b);
}
